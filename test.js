class Player {
    constructor(name, species, hp, mp) {
        this.name = name;
        this.species = species;
        this.hp = hp;
        this.mp = mp;
    }
    getName() {
        return this.name;
    }
    getHp() {
        return this.hp;
    }
    getmp() {
        return this.mp;
    }

}